package video3;

public class MotorCycle {

	private int engineSize;
	private int speed;
	
	public int getEngineSize() {
		return engineSize;
	}
	public MotorCycle setEngineSize(int engineSize) {
		this.engineSize = engineSize;
		return this;
		
	}
	public int getSpeed() {
		return speed;
	}
	public MotorCycle setSpeed(int speed) {
		this.speed = speed;
		return this;
	}
	
	
	
}
